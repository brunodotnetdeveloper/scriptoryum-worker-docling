CREATE TABLE public.document_chunks (
	id int4 GENERATED BY DEFAULT AS IDENTITY( INCREMENT BY 1 MINVALUE 1 MAXVALUE 2147483647 START 1 CACHE 1 NO CYCLE) NOT NULL,
	document_id int4 NOT NULL,
	chunk_index int4 NOT NULL,
	"content" text NULL,
	embedding public.vector NULL,
	created_at timestamptz NOT NULL,
	CONSTRAINT "PK_document_chunks" PRIMARY KEY (id),
	CONSTRAINT "FK_document_chunks_documents_document_id" FOREIGN KEY (document_id) REFERENCES public.documents(id) ON DELETE CASCADE
);
CREATE INDEX "IX_document_chunks_document_id" ON public.document_chunks USING btree (document_id);
CREATE INDEX "IX_document_chunks_embedding" ON public.document_chunks USING ivfflat (embedding);