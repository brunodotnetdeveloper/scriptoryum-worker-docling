CREATE TABLE public.documents (
	id int4 GENERATED BY DEFAULT AS IDENTITY( INCREMENT BY 1 MINVALUE 1 MAXVALUE 2147483647 START 1 CACHE 1 NO CYCLE) NOT NULL,
	processed_file_name varchar(500) NOT NULL,
	description varchar(2000) NULL,
	storage_provider text NOT NULL,
	storage_path varchar(1000) NOT NULL,
	file_type text NOT NULL,
	uploaded_by_user_id text NULL,
	uploaded_at timestamptz DEFAULT now() NOT NULL,
	status text DEFAULT 'Uploaded'::text NOT NULL,
	created_at timestamptz DEFAULT now() NOT NULL,
	updated_at timestamptz DEFAULT now() NOT NULL,
	text_extracted text NULL,
	original_file_name varchar(500) DEFAULT ''::character varying NOT NULL,
	file_size int8 DEFAULT 0 NOT NULL,
	summary text DEFAULT ''::text NULL,
	processing_started_at timestamptz NULL,
	CONSTRAINT p_k_documents PRIMARY KEY (id),
	CONSTRAINT f_k_documents_asp_net_users_uploaded_by_user_id FOREIGN KEY (uploaded_by_user_id) REFERENCES public.asp_net_users(id) ON DELETE RESTRICT
);
CREATE INDEX i_x_documents_uploaded_by_user_id ON public.documents USING btree (uploaded_by_user_id);


